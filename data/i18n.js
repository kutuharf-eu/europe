// Zentrale Übersetzungen (DE Standard, TR & EN). Rein clientseitig via LocaleProvider,
// deshalb bleiben die URLs unverändert und Deutsch bleibt die kanonische SEO-Sprache.
export const LOCALES = ['de', 'tr', 'en'];
export const DEFAULT_LOCALE = 'de';
export const LOCALE_LABELS = { de: 'DE', tr: 'TR', en: 'EN' };

export function translate(locale, key, vars, fallback) {
  const pick = (loc) => key.split('.').reduce((o, k) => (o == null ? undefined : o[k]), TRANSLATIONS[loc]);
  let val = pick(locale);
  if (val == null) val = pick(DEFAULT_LOCALE);
  if (val == null) return fallback != null ? fallback : key;
  if (vars) for (const [k, v] of Object.entries(vars)) val = String(val).split(`{${k}}`).join(v);
  return val;
}

